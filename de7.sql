--Tháng mấy trong năm 2006, doanh số bán hàng cao nhất ?
SELECT TOP 1 MONTH(NGHD) AS Thang, SUM(TRIGIA) AS DoanhSo
FROM HOADON
WHERE YEAR(NGHD) = 2006
GROUP BY MONTH(NGHD)
ORDER BY SUM(TRIGIA) DESC;
--Tìm sản phẩm (MASP, TENSP) có tổng số lượng bán ra thấp nhất trong năm 2006.
SELECT MASP, TENSP
FROM SANPHAM
WHERE MASP = (
    SELECT TOP 1 MASP
    FROM CTHD
    JOIN HOADON ON CTHD.SOHD = HOADON.SOHD
    WHERE YEAR(HOADON.NGHD) = 2006
    GROUP BY MASP
    ORDER BY SUM(SL) ASC
);
--*Mỗi nước sản xuất, tìm sản phẩm (MASP,TENSP) có giá bán cao nhất.
WITH GIACAOONHAT_THEONUOC AS (
    SELECT NUOCSX, MAX(GIA) AS MaxPrice
    FROM SANPHAM
    GROUP BY NUOCSX
)
SELECT S.NUOCSX, S.MASP,S.TENSP, G.MaxPrice
FROM SANPHAM S
INNER JOIN GIACAOONHAT_THEONUOC G ON S.NUOCSX = G.NUOCSX AND S.GIA = G.MaxPrice;
--Tìm nước sản xuất sản xuất ít nhất 3 sản phẩm có giá bán khác nhau.
SELECT NUOCSX
FROM SANPHAM
GROUP BY NUOCSX
HAVING COUNT(DISTINCT GIA) >= 3;
--*Trong 10 khách hàng có doanh số cao nhất, tìm khách hàng có số lần mua hàng nhiều nhất.
WITH RankedKH AS (
    SELECT HOADON.MAKH, COUNT(*) AS SoLanMua,
           ROW_NUMBER() OVER (ORDER BY SUM(TRIGIA) DESC) AS RowNum
    FROM HOADON
    GROUP BY HOADON.MAKH
)
SELECT KHACHHANG.MAKH, KHACHHANG.HOTEN, RankedKH.SoLanMua
FROM RankedKH
JOIN KHACHHANG ON KHACHHANG.MAKH = RankedKH.MAKH
WHERE RowNum <= 10
ORDER BY RankedKH.SoLanMua DESC;


